        subroutine ortho2_stdtosimplex(x,y,uout,vout)
        implicit real *8 (a-h,o-z)
c
c       map the standard triangle to the simplex
c
        sqrt3=sqrt(3.0d0)
        uout=0.5d0*(x+1)-0.5d0/sqrt3*(y+1/sqrt3)
        vout=            1.0d0/sqrt3*(y+1/sqrt3)
c
        return
        end
