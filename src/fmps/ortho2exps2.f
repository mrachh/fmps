        umatr(j,i)=tmp
c
        tmp=vmatr(i,j)
        vmatr(i,j)=vmatr(j,i)
        vmatr(j,i)=tmp        
 1400 continue
 1600 continue
c
        call orthom(umatr,npols,work,rcond)
cccc        call prin2('rcond=*',rcond,1)
c
        return
        end
