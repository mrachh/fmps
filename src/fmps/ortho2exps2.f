        x=vert1(1)+u*(vert2(1)-vert1(1))+v*(vert3(1)-vert1(1))
        y=vert1(2)+u*(vert2(2)-vert1(2))+v*(vert3(2)-vert1(2))
c
        z(1)=x
        z(2)=y
        call ortho2eva(norder,z,pols,work)
c
        return
        end
